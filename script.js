regionShapes.forEach(shape => {
  shape.addEventListener('mouseenter', () => {
    const region = regionMapData.find(r => r.id === shape.dataset.region);
    if (!region) return;
    const area = shape.querySelector('rect, path');
    if (area) area.style.opacity = '0.8';
  });
  shape.addEventListener('mouseleave', () => {
    const region = regionMapData.find(r => r.id === shape.dataset.region);
    if (!region) return;
    const area = shape.querySelector('rect, path');
    if (area) area.style.opacity = '1';
  });
  shape.addEventListener('click', () => {
    const region = regionMapData.find(r => r.id === shape.dataset.region);
    if (region) selectRegion(region);
  });
  shape.addEventListener('keydown', event => {
    if (event.key === 'Enter' || event.key === ' ') {
      event.preventDefault();
      const region = regionMapData.find(r => r.id === shape.dataset.region);
      if (region) selectRegion(region);
    }
  });
});

window.addEventListener('resize', () => {
  resizeCanvas();
  drawChart();
});

function init() {
  currentLanguage = navigator.language.startsWith('zh') ? 'zh' : navigator.language.startsWith('en') ? 'en' : 'ja';
  languageSelect.value = currentLanguage;
  translatePage();
  setMapColors();
  setupDragAndDrop();
  setupShareButton();
  resizeCanvas();
  drawChart();
  updateCleanupImpact();
  if (!selectedRegion && regionMapData.length > 0) {
    selectRegion(regionMapData[0]);
  }
}

init();
